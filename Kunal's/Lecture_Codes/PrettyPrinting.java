package Lecture_Codes;

public class PrettyPrinting {
    public static void main(String[] args){
        float a = 453.1274f;
        System.out.printf("Formated number is %.2f\n", a);
        System.out.printf("PI: %.3f\n", Math.PI);
        System.out.printf("Hello my name is %s and I am %s \n", "Aadarsh", "Fulfilled");
        System.out.println((char)('a'+3));
    }
}